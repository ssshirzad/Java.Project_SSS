package day.one;

public class StringCaseChangeMethods {
	
	// String Class Methods
	// Methods are functions doing specific tasks

	public static void main(String[] args) {
		String gender = "male"; // = MALE
		// change all the letters to be upper case in String
		
		// This is called String manipulation
		// toUpperCase() method is used to change all the letter
		// to UpperCase
		String genderInUpperCase = gender.toUpperCase();
		System.out.println(genderInUpperCase);
		
		// How to change the letters to lower case
		String city = "LONDON";
		// once you create a new string and type the variable followed
		// by a dot, Eclips will pull all the options you have to 
		// manipulate that String
		String cityInLowerCase = city.toLowerCase();
		System.out.println(cityInLowerCase);
		
	
		
		
		
		
		
	}
	
	

}
