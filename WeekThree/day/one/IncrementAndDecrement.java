package day.one;

public class IncrementAndDecrement {
	public static void main(String[] args) {
		// increment => increasing or adding by 1 ++
		// decrement => decrease subtracting by 1 --
		
		int number = 10;
		
		 number = number + 1;
		 System.out.println(number);
		 
		 number += 1;
		 System.out.println(number);
		 
		 number ++;
		 System.out.println(number);
		 
		 
		// decrementing number by 1
		 
			
		 number = number - 1;
		 System.out.println(number);
		 
		 number -= 1;
		 System.out.println(number);
		 
		 number --;
		 System.out.println(number);
		 
	}

}
