package day.three;

public class Loops {

	//Loops for repeating a block of code and an specific time.
	// for loop
	// for each (Enhance Loop)
	// while loop
	// do-while loop
	
	public static void main(String[] args) {
		// print numbers between 1 to 100. 
		// increment
		// start point = 1
		// end point = 5
		// initial point / Start point -> declare and assign a variable. 
		// end point -> a condition against the variable you created. 
		// increment / decrement
		for(int start = 1 ; start <= 100 ; start++) {	
			System.out.println(start);	
		}
	}

}
