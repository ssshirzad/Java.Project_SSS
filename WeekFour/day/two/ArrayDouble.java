package day.two;

public class ArrayDouble {
	public static void main(String[] args) {
		
		double[] digit = new double[4];
		
		digit[0] = 12.99;
		digit[1] = 13.99;
		digit[2] = 14.99;
		digit[3] = 15.99;
		
		double result = digit[0] + digit[1] + digit[2] + digit[3];
		System.out.println(result);
		
	}

}
