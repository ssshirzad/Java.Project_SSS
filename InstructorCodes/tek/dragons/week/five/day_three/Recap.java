package tek.dragons.week.five.day_three;

public class Recap {
	public static void main(String[] args) {
		
		//While loop. 
		//count to 100 from 0;
		int counter = 0; 
		while(counter < 100) {
			System.out.println(counter);
			counter ++; 
		}
		
		do {
			System.out.println(counter);
			counter++;
		}while(counter < 200);
		
	}
}
