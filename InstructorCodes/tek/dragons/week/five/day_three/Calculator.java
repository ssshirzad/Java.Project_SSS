package tek.dragons.week.five.day_three;

public class Calculator {
	//create a method.
	// that return the adding result of number 10 and 20. 
	//method parameters are variable declared into the method signature ().
	//if a method have parameters then whenever we use that 
	//method we have pass value as arguments. 
	public int doAdding(int firstNumber , int secondNumber) {
		int result = firstNumber + secondNumber;
		return result;
	}
	
	public double doAddingDouble(double firstNumber, double secondNumber) {
		double result = firstNumber + secondNumber; 
		return result;
	}
}
