package tek.dragons.week.three.day_three;

public class Activity2 {
	// write a program that take a positive integer
	// and output if number is odd or even.
	// 7 minute
	public static void main(String[] args) {
		// if remainder a number / 2 = 0 even. otherwise it's odd.
		int number = 250;
		if (number % 2 == 0) {
			System.out.println(number + " is even");
		}else {
			System.out.println(number + " is odd");
		}
		
	}
}
