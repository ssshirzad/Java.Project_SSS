package tek.dragons.week.one.day.three;

public class DataTypeSpecification {
	
	public static void main(String[] args) {
		// int number1 = 102.3; //This is error
		
		//long numbers should end with L at the end. 
		long number2 = 123456789123L;
		
		//Float numbers should end with F at the end. 
		float orangePrice = 1.2F;
		
		int number3 = 32000;

		int number4 = 45000;
		
		String date = "01/11/2023";
		
		String email = "myemail123@gmail.com";
		
		char letter = 'A';
		
		// F for Female  M for Male. 
		char gender = 'F';
		
		// A B C F E
		char grade = 'E';
		
		
		//Type Declaring Variables.
		//1) Declare and Assign right away.
		// = is for Assignment
		int age = 30; 
		//Declare only. 
		int numberA; 
		
		// assignment
		numberA = 190;
		
		//Calling Variable 
		System.out.println(numberA);
		
	}

}









