public class Recap {
	/*
	 * OOPs Concepts (Object Oriented Programming Concepts)
	 * - Encapsulation: mechanism for hiding data in the class by making 
	 * instance variables (class variables) private and providing in-direct access with 
	 * getters and setters. 
	 * - Inheritance: sub-classes inherit class variables and methods of super-classes. 
	 * - Polymorphism : a method / constructor can have different forms. 
	 * 	* Overloading: happening in the same class with different parameters. 
	 * 	* Overriding: happening in inheritance while a sub-class override implementation 
	 * 		of a method in super-class. 
	 * - Abstraction: a mechanism to restrict object creation of a class. 
	 * 	* abstract classes: partial abstract and have to be used in inheritance. 
	 * 	* interface: fully abstract and have to implements them to classes. 
	 */
}
